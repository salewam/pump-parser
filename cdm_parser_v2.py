def main():
    if len(sys.argv) < 2:
        print("Использование: python3 pump_parser_v30.py <input.pdf> [output.json]")
        sys.exit(1)

    pdf_path = sys.argv[1]
    output = sys.argv[2] if len(sys.argv) >= 3 else 'parsed.json'

    pumps = extract_from_pdf(pdf_path)
    models = set(p['id'] for p in pumps)

    print(f"V32: {len(pumps)} записей, {len(models)} моделей")

    if pumps:
        with open(output, 'w', encoding='utf-8') as f:
            json.dump(pumps, f, ensure_ascii=False, indent=2)
        print(f"Сохранено: {output}")

        # Статистика
        by_series = defaultdict(lambda: {'records': 0, 'models': set()})
        for p in pumps:
            m = re.search(r'(\d+)', p['id'])
            if m:
                by_series[m.group(1)]['records'] += 1
                by_series[m.group(1)]['models'].add(p['id'])

        print("\nПо сериям:")
        for s in sorted(by_series.keys(), key=lambda x: int(x)):
            print(f"  {s}: {len(by_series[s]['models'])} моделей, {by_series[s]['records']} записей")
    else:
        print("Данные не найдены!")


if __name__ == '__main__':
    main()
